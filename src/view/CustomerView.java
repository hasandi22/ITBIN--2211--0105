    //Handle mouse click events on the table to populate fields with the selected row data.
    private void tableMouseClicked(java.awt.event.MouseEvent evt) {
    selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        String id = (String) tableModel.getValueAt(selectedRow, 0);
        String name = (String) tableModel.getValueAt(selectedRow, 1);
        String gender = (String) tableModel.getValueAt(selectedRow, 2);
        String password = (String) tableModel.getValueAt(selectedRow, 3);
        
        jTextField6.setText(id);
        jTextField4.setText(name);
        jTextField5.setText(gender);
        jTextField1.setText(password);
    }
}

   //Add the selected customer. 
    private void addCustomer() {
    String id = jTextField6.getText();
    String name = jTextField4.getText();
    String gender = jTextField5.getText();
    String passwordText = jTextField1.getText();

    if (name.isEmpty() || gender.isEmpty() || passwordText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields must be filled in.");
        return;
    }
    
    customerController.addCustomer(id, name, gender, passwordText);
        loadCustomer();
        clearFields();
    }
    
    //Delete the selected customer.
    private void deleteCustomer() {
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow >= 0) {
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this Customer", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String id = (String) tableModel.getValueAt(selectedRow, 0);
            customerController.deleteCustomer(id);
            loadCustomer();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a customer to delete.");
    }
    }
   
    //update the selected customer with the data from input fields.
   private void updateCustomer() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String id = (String) tableModel.getValueAt(selectedRow, 0);
            String name = jTextField4.getText();
            String gender = jTextField5.getText();
            String passwordText = jTextField1.getText();

            // Update customer in the controller
            customerController.updateCustomer(id, name, gender, passwordText);

            // Update the row in the table
            tableModel.setValueAt(name, selectedRow, 1);
            tableModel.setValueAt(gender, selectedRow, 2);
            tableModel.setValueAt(passwordText, selectedRow, 3);
            
            
          //clear feilds
          clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row to update.");
        }
    }



  //clear feilds

private void clearFields() {
        jTextField6.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField1.setText("");
        
    }
    
    
    

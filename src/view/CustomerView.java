    //Load customers from the controller and display them in the table.
    public void loadCustomer() {
    if (tableModel != null) { // Check if tableModel is initialized
        CustomerController controller = new CustomerController(this);
        List<CustomerModel> customers = controller.getAllCustomers();
        
        
        
        if (customers != null) { // Check if customers list is not null
            tableModel.setRowCount(0); // Clear existing data

            for (CustomerModel customer : customers) { // Iterate through customers list
                tableModel.addRow(new Object[]{
                        customer.getId(), customer.getName(), customer.getGender(), customer.getPassword() // Add each customer to tableModel
                });
            }
        }
    }
}
    
    
    
